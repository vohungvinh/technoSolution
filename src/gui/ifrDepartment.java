    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
//        int deptId = Integer.parseInt(txtDepartmentNumber.getText());
        String deptName = txtDepartmentName.getText().toString();
        int financeMN = Integer.parseInt(txtFinanceManage.getText());
        if(!EmpName.containsKey(financeMN)){
            JOptionPane.showMessageDialog(this, "finance id not exist");
            return;
        }
        String decription = txpDecription.getText().toString();
        DepartmentDTO dept = new DepartmentDTO(financeMN, deptName, decription);
        if(deptPro.create(dept)){
            JOptionPane.showMessageDialog(this, "create success");
            readAll();
        }else{
            JOptionPane.showMessageDialog(this, deptPro.error);
        }
    }//GEN-LAST:event_btnCreateActionPerformed

