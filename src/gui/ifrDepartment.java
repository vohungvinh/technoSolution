    private void readAll(){
        
        ArrayList<DepartmentDTO> list = deptPro.readAll();
        Vector cols = new Vector();
        cols.add("id");
        cols.add("name");
        cols.add("finance Manageer");
        cols.add("decription");
        cols.add("finance Manager Name");
        Vector rows = new Vector();
        for (DepartmentDTO departmentDTO : list) {
            Vector row = new Vector();
            row.add(departmentDTO.getDepartmentNumber());
            row.add(departmentDTO.getDepartmentName());
            row.add(departmentDTO.getFinanceMN());
            row.add(departmentDTO.getDecription());
            row.add(EmpName.get(departmentDTO.getFinanceMN()));
            System.out.println("EmpName = " + EmpName.get(1));
            rows.add(row);  
        }
        tblMain.setModel(new DefaultTableModel(rows, cols){
            @Override
            public boolean isCellEditable(int i, int i1) {
                return false; //To change body of generated methods, choose Tools | Templates.
            }
            
        });
        tblMain.updateUI();
        srcMain.setViewportView(this.tblMain);
    }
    private void tblMainSelectedItem(){
        model = tblMain.getSelectionModel();
        model.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(!model.isSelectionEmpty()){
                    int index = model.getMinSelectionIndex();
                    txtDepartmentNumber.setText(tblMain.getValueAt(index, 0).toString());
                    txtDepartmentName.setText(tblMain.getValueAt(index, 1).toString());
                    txtFinanceManage.setText(tblMain.getValueAt(index, 2).toString());
                    txpDecription.setText(tblMain.getValueAt(index, 3).toString());
                    txtFinanceName.setText(tblMain.getValueAt(index, 4).toString());
                }
            }
        });
    }
